$('g, .next, .previous').on('click', function (e) {
  $('#res-sect').show();
  var selectedCountryName = $(this).data('country');
  $('html, body').animate({
    scrollTop: $("#res-sect").offset().top
  }, 'slow');
  $('.results-section, .country-name, .arrows-sect').fadeOut().fadeIn(500);
  handleData($(this).data('country'));
  $('.country-name').text(selectedCountryName);
  $('.country-name').attr('data-country', selectedCountryName);
  var nextAndPreviousCountries = inArray(selectedCountryName, countries);
  $('.next').html(nextAndPreviousCountries.nextCountry + '<img class="next-arrow" src="assets/next-arrow.png" />').data('country', nextAndPreviousCountries.nextCountry);
  $('.previous').html('<img class="previous-arrow" src="assets/previous-arrow.png" />' + nextAndPreviousCountries.previousCountry).data('country', nextAndPreviousCountries.previousCountry);
  e.preventDefault();
});

// Handling all data to be rendered
function handleData(countryName) {
  $.each(jsonData, function (index, value) {
    if (value.name == countryName) {
      renderData(value.scope_asset_disclosure);
      renderData(value.content_asset_declarations);
      renderData(value.public_access_to_asset_declarations);
      renderData(value.bodies_responsible_for_storing_and_managing_asset_declaration_data);
      renderData(value.verification_and_control_of_asset_information);
      renderData(value.sanctions_for_non_declaration_and_incorrect_information);
      $('#timeline_for_storing_of_asset_declaration_information').html('<table><tr><th>' + value.timeline_for_storing_of_asset_declaration_information + '</th></tr></table>');
      renderData(value.clarity_of_asset_disclosure_legislation);
      renderData(value.means_of_submission_of_asset_declarations);
      renderData(value.openness_of_asset_declarations);
      return;
    }
  });
}

// Rendering specific section's data into html
function renderData(sectionJSONName) {
  $.each(sectionJSONName, function (key, value) {

    var finalValue = "";
    if (value == 'yes') {
      finalValue = '<img class="shape" width="25px" src="assets/yes-shape.png" />';
    } else if (value == 'no') {
      finalValue = '<img class="shape" width="20px" src="assets/no-shape.png" />';
    } else if (value == '' || value === undefined) {
      finalValue = 'Unknown';
    } else {
      finalValue = value;
    }

    $('#' + key).html(finalValue);
    $('.' + key).html(finalValue);
  });
}

// If country in array get the next and previous countries
function inArray(country, array) {
  var arrayLength = array.length;
  for (var i = 0; i < arrayLength; i++) {
    if (country == array[i]) {
      var nextCountry = array[i + 1];
      var previousCountry = array[i - 1];
      if (array[i] == "Albania") {
        previousCountry = array[arrayLength - 1]
      }
      if (array[i] == "Ukraine") {
        nextCountry = array[0]
      }
      return {'nextCountry': nextCountry, 'previousCountry': previousCountry};
    }
  }
}


